#include"libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*a;
	int		i;

	a = (char *)b;
	i = 0 ;
	while (i < (int)len)
	{
		a[i] = c;
		i++;
	}
	return (b);
}
